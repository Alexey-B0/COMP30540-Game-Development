public class Model {

	private GameObject Player;
	private Controller controller = Controller.getInstance();
	private CopyOnWriteArrayList<GameObject> FishList = new CopyOnWriteArrayList<GameObject>();
	private CopyOnWriteArrayList<GameObject> EnemiesList = new CopyOnWriteArrayList<GameObject>();
	private CopyOnWriteArrayList<GameObject> BulletList = new CopyOnWriteArrayList<GameObject>();
	private int Score = 0;
	private int Lives = 3;
	private int screenWidth = 0;
	private int screenHeight = 0;

	private long lastFishSpawnTime = 0;
	private final long FISH_SPAWN_INTERVAL = 500;
	private long lastEnemySpawnTime = 0;
	private final long ENEMY_SPAWN_INTERVAL = 1000;
	private final int ENTITY_SPEED = 5;

	static Random random = new Random();

	public Model() {
		setup();
	}

	// This is the heart of the game , where the model takes in all the inputs
	// ,decides the outcomes and then changes the model accordingly.
	public void gamelogic(Dimension size) {
		setScreenWidth((int)size.getWidth());
		setScreenHeight((int)size.getHeight());
		// Player Logic first
		playerLogic();
		// fish Logic next
		fishLogic();
		// enemy logic next
		enemyLogic();
		// Bullets move next
		bulletLogic();
		// interactions between objects
		gameLogic();

	}

	private void gameLogic() {
		// score counter for fish caught
		for (GameObject temp : FishList) {
			if (Math.abs(temp.getCentre().getX() - Player.getCentre().getX()) < temp.getWidth()
					&& Math.abs(temp.getCentre().getY() - Player.getCentre().getY()) < temp.getHeight()) {
				FishList.remove(temp);
				Score++;
			}
		}

		// life counter for enemy hit
		for (GameObject temp : EnemiesList) {
			if (Math.abs(temp.getCentre().getX() - Player.getCentre().getX()) < temp.getWidth()
					&& Math.abs(temp.getCentre().getY() - Player.getCentre().getY()) < temp.getHeight()) {
				EnemiesList.remove(temp);
				Lives--;
			}
		}

	}

	private void fishLogic() {
		for (GameObject temp : FishList) {
			// Move enemies
			temp.getCentre().ApplyVector(temp.getDirectionalVector());

			if ((temp.getDirectionalVector().getX() > 0 && temp.getCentre().getX() >= 900)
					|| (temp.getDirectionalVector().getX() < 0 && temp.getCentre().getX() <= 0)) {
				FishList.remove(temp);
			}
		}

		long now = System.currentTimeMillis();
		if (FishList.size() < 20 && (now - lastFishSpawnTime) >= FISH_SPAWN_INTERVAL) {
				FishList.add(spawnFish());
				lastFishSpawnTime = now;
		}
	}

	private GameObject spawnFish() {
		boolean spawnLeft = random.nextBoolean(); // Randomly choose left (true) or right (false)
		float xPos = spawnLeft ? (float) (Math.random() * 50) : (float) (getScreenWidth() - Math.random() * 50);
		Vector3f direction = spawnLeft ? new Vector3f(1, 0, 0) : new Vector3f(-1, 0, 0);
		String texture = spawnLeft ? "res/fish_walk_right.png" : "res/fish_walk_left.png";

		return new GameObject(texture, 50, 50, new Point3f(xPos, ((float) Math.random() * 400 + 200), 0, getScreenWidth()), direction.byScalar(ENTITY_SPEED));
	}

	private void enemyLogic() {
		for (GameObject enemy : EnemiesList) {
			enemy.getCentre().ApplyVector(enemy.getDirectionalVector());

			if ((enemy.getDirectionalVector().getX() > 0 && enemy.getCentre().getX() >= 900)
					|| (enemy.getDirectionalVector().getX() < 0 && enemy.getCentre().getX() <= 0)) {
				EnemiesList.remove(enemy);
			}
		}
			long now = System.currentTimeMillis();
			if (EnemiesList.size() < 5 && (now - lastEnemySpawnTime) >= ENEMY_SPAWN_INTERVAL) {
					EnemiesList.add(spawnEnemy());
					lastEnemySpawnTime = now;
			}
	}

	private GameObject spawnEnemy() {
		boolean spawnLeft = random.nextBoolean(); // Randomly choose left (true) or right (false)
		float xPos = spawnLeft ? (float) (Math.random() * 50) : (float) (getScreenWidth() - Math.random() * 50);
		Vector3f direction = spawnLeft ? new Vector3f(1, 0, 0) : new Vector3f(-1, 0, 0);
		String texture = spawnLeft ? "res/enemy_walk_right.png" : "res/enemy_walk_left.png";

		return new GameObject(texture, 50, 50, new Point3f(xPos, ((float) Math.random() * 400 + 200), 0, getScreenWidth()), direction.byScalar(1));
	}

	private void bulletLogic() {
		for (GameObject Bullet : BulletList) {
			Bullet.getCentre().ApplyVector(Bullet.getDirectionalVector().byScalar(2));

			if (Bullet.getCentre().getY() == 0 || Bullet.getCentre().getX() == 0
					|| Bullet.getCentre().getX() == 900 || Bullet.getCentre().getY() >= 600) {
				BulletList.remove(Bullet);
			}
		}
	}

	private void playerLogic() {

		// smoother animation is possible if we make a target position // done but may
		// try to change things for students

		// check for movement and if you fired a bullet

		if (Controller.getInstance().isKeyAPressed()) {
			Player.getCentre().ApplyVector(new Vector3f(-2, 0, 0));
		}

		if (Controller.getInstance().isKeyDPressed()) {
			Player.getCentre().ApplyVector(new Vector3f(2, 0, 0));
		}

		// if (Controller.getInstance().isKeyWPressed()) {
		// Player.getCentre().ApplyVector(new Vector3f(0, 2, 0));
		// }

		// if (Controller.getInstance().isKeySPressed()) {
		// Player.getCentre().ApplyVector(new Vector3f(0, -2, 0));
		// }

		if (Controller.getInstance().isMousePressed()) {
			CreateBullet();
			Controller.getInstance().setMousePressed(false);
		}

		if (Controller.getInstance().getMouseCurrLocation().getY() != Player.getCentre().getY()) {
			Player.getCentre().setY(Controller.getInstance().getMouseCurrLocation().getY());
		}

	}

	private void CreateBullet() {
		Point3f mousePosition = Controller.getInstance().getMousePosition();
		Vector3f BulletVector = new Vector3f(mousePosition.getX() - Player.getCentre().getX(),
				Player.getCentre().getY() - mousePosition.getY(), 0).Normal();

		GameObject Bullet = new GameObject("res/Bullet.png", 32, 64,
				new Point3f(Player.getCentre().getX(), Player.getCentre().getY(), 0.0f), BulletVector);

		BulletList.add(Bullet);
	}

	public GameObject getPlayer() {
		return Player;
	}

	public CopyOnWriteArrayList<GameObject> getEnemies() {
		return EnemiesList;
	}

	public CopyOnWriteArrayList<GameObject> getFish() {
		return FishList;
	}

	public CopyOnWriteArrayList<GameObject> getBullets() {
		return BulletList;
	}

	public int getScore() {
		return Score;
	}

	public void setScore(int newScore) {
		this.Score = newScore;
	}

	public int getLives() {
		return Lives;
	}

	public void setLives(int newLives) {
		this.Lives = newLives;
	}

	public void resetScore() {
		setScore(0);
	}

	public int getScreenWidth() {
		return screenWidth;
	}

	public void setScreenWidth(int screenWidth) {
		this.screenWidth = screenWidth;
	}

	public int getScreenHeight() {
		return screenHeight;
	}

	public void setScreenHeight(int screenHeight) {
		this.screenHeight = screenHeight;
	}

	public void setup() {
		FishList.clear();
		EnemiesList.clear();
		BulletList.clear();
		Player = new GameObject("res/itemsfishinga.png", 50, 50, new Point3f(500, 500, 0));
		resetScore();
		if(getLives() < 3) {
			setLives(Lives + 1);
		}
	}

}

